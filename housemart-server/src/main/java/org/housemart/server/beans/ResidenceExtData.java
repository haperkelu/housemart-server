package org.housemart.server.beans;

public class ResidenceExtData {

	private int residenceId;
	private int countSale;
	private int countRent;
	public int getResidenceId() {
		return residenceId;
	}
	public void setResidenceId(int residenceId) {
		this.residenceId = residenceId;
	}
	public int getCountSale() {
		return countSale;
	}
	public void setCountSale(int countSale) {
		this.countSale = countSale;
	}
	public int getCountRent() {
		return countRent;
	}
	public void setCountRent(int countRent) {
		this.countRent = countRent;
	}
	
}
